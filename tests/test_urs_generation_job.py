"""
tests/test_urs_generation_job.py — Regression tests for background URS AI
generation (services/urs_generation_job.py).

Root cause under test: routes/urs.py's /generate endpoint used to iterate
generate_content_stream() *inside* the Flask request, holding a gunicorn
worker for the whole generation time. On large section selections this
exceeded Render's `--timeout=60` and the worker was SIGKILLed mid-stream
([CRITICAL] WORKER TIMEOUT). These tests assert the new behavior instead:
submit_generation_job() returns immediately no matter how long Gemini takes,
progress/results are visible by polling SQLite, one bad batch never loses
requirements already generated by other batches, and — per a follow-up
hardening pass — malformed/truncated output is retried and salvaged rather
than failing the batch outright, while genuine API errors and safety blocks
are never retried.
"""

import json
import time

import pytest

from pharmagpt import urs_database as udb
from pharmagpt.services import urs_generation_job as gen_job
from google.genai import types


class _FakeModels:
    def __init__(self, fn):
        self._fn = fn

    def generate_content(self, model, contents, config):
        return self._fn(model, contents, config)


class _FakeClient:
    def __init__(self, fn):
        self.models = _FakeModels(fn)


class _FakeUsage:
    def __init__(self, prompt_tokens=100, response_tokens=200):
        self.prompt_token_count = prompt_tokens
        self.candidates_token_count = response_tokens


class _FakeCandidate:
    def __init__(self, finish_reason=types.FinishReason.STOP):
        self.finish_reason = finish_reason


class _FakeResponse:
    def __init__(self, text, prompt_tokens=100, response_tokens=200, finish_reason=types.FinishReason.STOP):
        self.text = text
        self.usage_metadata = _FakeUsage(prompt_tokens, response_tokens)
        self.candidates = [_FakeCandidate(finish_reason)]


def _make_urs():
    return udb.create_urs({
        "title": "URS - Autoclave", "equipment_name": "Autoclave-01",
        "equipment_type": "Autoclave", "category": "Equipment",
    }, company_id="test-company-1")


def _req_json(section):
    return json.dumps([{
        "section": section, "requirement": "The system shall do X.",
        "rationale": "GMP", "priority": "High", "gmp_criticality": "GMP-Critical",
        "regulatory_ref": "21 CFR Part 11", "verification_method": "Functional Test",
        "acceptance_criteria": "Pass/fail",
    }])


def _wait_for_terminal_generation(urs_id, timeout=10.0):
    deadline = time.monotonic() + timeout
    status = None
    while time.monotonic() < deadline:
        status = udb.get_generation_status(urs_id)
        if status["generation_status"] in ("completed", "failed"):
            return status
        time.sleep(0.05)
    pytest.fail(f"generation job never reached a terminal status: {status}")


def test_submit_generation_job_returns_immediately_and_completes_in_background(db_path, monkeypatch):
    urs = _make_urs()

    def fake_generate_content(model, contents, config):
        time.sleep(0.05)  # simulated network latency, kept small for test speed
        return _FakeResponse(_req_json("Functional Requirements"))

    monkeypatch.setattr(gen_job, "gemini_client", _FakeClient(fake_generate_content))

    start = time.monotonic()
    gen_job.submit_generation_job(urs["id"], urs, ["Functional Requirements", "Safety Requirements"])
    submit_elapsed = time.monotonic() - start

    # The whole point of this redesign: submitting must not block on Gemini.
    assert submit_elapsed < 0.5

    # Default URS_GENERATION_BATCH_SIZE (2) puts both sections in one batch.
    status = udb.get_generation_status(urs["id"])
    assert status["generation_status"] == "running"
    assert status["generation_progress_total"] == 1

    final = _wait_for_terminal_generation(urs["id"])
    assert final["generation_status"] == "completed"
    assert final["generation_result_count"] == 1

    saved = udb.get_requirements(urs["id"])
    assert len(saved) == 1
    assert saved[0]["section"] == "Functional Requirements"
    assert saved[0]["req_id"]  # numbered by section prefix

    # The fake only ever returns "Functional Requirements" content, so
    # section-coverage tracking should correctly report Safety as missing
    # even though the batch itself didn't raise.
    assert final["generation_message"] == (
        "1 of 2 sections generated successfully (1 requirements). "
        "1 section failed and can be retried: Safety Requirements."
    )


def test_run_generation_job_continues_after_one_batch_fails(db_path, monkeypatch):
    urs = _make_urs()

    def fake_generate_content(model, contents, config):
        prompt_text = contents[0].parts[0].text
        if "Safety Requirements" in prompt_text:
            raise RuntimeError("simulated Gemini error")
        return _FakeResponse(_req_json("Functional Requirements"))

    monkeypatch.setattr(gen_job, "gemini_client", _FakeClient(fake_generate_content))

    batches = [["Functional Requirements"], ["Safety Requirements"]]
    gen_job._run_generation_job(urs["id"], urs, batches)

    status = udb.get_generation_status(urs["id"])
    # One batch succeeded — the job is "completed" (failure surfaced via
    # generation_error / generation_message) rather than discarding the
    # partial result.
    assert status["generation_status"] == "completed"
    assert status["generation_result_count"] == 1
    assert "Batch 2" in status["generation_error"]
    assert status["generation_message"] == (
        "1 of 2 sections generated successfully (1 requirements). "
        "1 section failed and can be retried: Safety Requirements."
    )

    saved = udb.get_requirements(urs["id"])
    assert len(saved) == 1
    assert saved[0]["section"] == "Functional Requirements"


