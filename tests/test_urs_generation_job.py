def test_generate_batch_retries_on_max_tokens_truncation_then_succeeds(db_path, monkeypatch):
    urs = _make_urs()
    calls = []

    def fake_generate_content(model, contents, config):
        calls.append(1)
        if len(calls) == 1:
            return _FakeResponse(
                '[{"section": "Functional Requirements", "requirement": "cut off',
                finish_reason=types.FinishReason.MAX_TOKENS,
            )
        return _FakeResponse(_req_json("Functional Requirements"))

    monkeypatch.setattr(gen_job, "gemini_client", _FakeClient(fake_generate_content))

    reqs, note = gen_job._generate_batch_resilient(urs["id"], 1, 1, urs, ["Functional Requirements"])

    assert len(calls) == 2  # retried once after the MAX_TOKENS truncation
    assert note is None     # second attempt parsed cleanly, no fallback needed
    assert len(reqs) == 1


def test_generate_batch_does_not_retry_safety_block(db_path, monkeypatch):
    urs = _make_urs()
    calls = []

    def fake_generate_content(model, contents, config):
        calls.append(1)
        return _FakeResponse("", finish_reason=types.FinishReason.SAFETY)

    monkeypatch.setattr(gen_job, "gemini_client", _FakeClient(fake_generate_content))

    with pytest.raises(gen_job.GenerationBlockedError):
        gen_job._generate_batch_resilient(urs["id"], 1, 1, urs, ["Functional Requirements"])

    assert len(calls) == 1  # never retried — retrying the same filter won't help


