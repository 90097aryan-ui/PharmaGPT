def test_generate_batch_falls_back_to_partial_extraction_after_retries_exhausted(db_path, monkeypatch):
    urs = _make_urs()
    calls = []
    truncated = (
        '[{"section": "Functional Requirements", "requirement": "The system shall do A.", '
        '"priority": "High", "gmp_criticality": "GMP"}, '
        '{"section": "Functional Requirements", "requirement": "The system shall do B'
    )

    def fake_generate_content(model, contents, config):
        calls.append(1)
        return _FakeResponse(truncated)  # always malformed on every attempt

    monkeypatch.setattr(gen_job, "gemini_client", _FakeClient(fake_generate_content))

    reqs, note = gen_job._generate_batch_resilient(urs["id"], 1, 1, urs, ["Functional Requirements"])

    assert len(calls) == 3  # 1 initial + 2 retries (config.URS_GENERATION_MAX_RETRIES default)
    assert note is not None
    assert len(reqs) == 1
    assert reqs[0]["requirement"] == "The system shall do A."


